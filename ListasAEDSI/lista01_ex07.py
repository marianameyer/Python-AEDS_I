# Crie um programa para ler dois números inteiros (num1 e num2) e imprimir a soma entre
# eles. Entretanto, mostre o que foi feito da seguinte maneira: 3 + 4 = 7

# Coleta de dados do usuário
num1 = int(input('Digite um número inteiro: '))
num2 = int(input('Digite outro número inteiro: '))

# Operação
soma = num1 + num2

# Saída
print(f'A soma dos dois números informados é: \n\n\t\t{num1} + {num2} = {soma}')
